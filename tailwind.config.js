const transToNegative = (obj) => {
  // 负值转换
  const __obj = {};
  for (const key in obj) {
    if (key === 0 || key === '0') {
      __obj[`${key}`] = `${obj[key]}`;
    } else {
      __obj[`-${key}`] = `-${obj[key]}`;
    }
  }
  return __obj;
};

